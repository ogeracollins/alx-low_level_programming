#include "main.h"

/**
 *add - Summation of two numbers
 *@a: First number
 *@b: Second number
 *
 *Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - Subtract
 *@a: First number
 *@b: Second number
 *
 *Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - Mulitply two numbers
 *@a: First number
 *@b: Second number
 *
 *Return: a - b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - Division
 *@a: First number
 *@b: Second number
 *
 *Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 *mod - Modulus of a number
 *@a: First number
 *@b: Second number
 *
 *Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
